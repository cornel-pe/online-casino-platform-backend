export interface INotification {
  _id?: string;
  userId?: string; // If null, it's a system-wide notification
  sentBy?: string; // Admin user who sent the notification
  type: NotificationType;
  title: string;
  message: string;
  data?: any; // Additional data for the notification
  priority: NotificationPriority;
  status: NotificationStatus;
  readAt?: Date;
  expiresAt?: Date;
  
  // Admin-specific fields
  isAdminOnly?: boolean; // Only visible to admins
  riskEventId?: string; // Link to risk event if this is a risk alert
  gameType?: string; // Game related to the notification
  gameId?: string; // Specific game ID
  actionRequired?: boolean; // Requires admin action
  autoGenerated?: boolean; // System-generated vs manual
  
  createdAt: Date;
  updatedAt: Date;
}

