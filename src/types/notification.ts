export enum NotificationPriority {
  LOW = 'low',
  NORMAL = 'normal',
  HIGH = 'high',
  URGENT = 'urgent'
}

export enum NotificationStatus {
  PENDING = 'pending',
  SENT = 'sent',
  DELIVERED = 'delivered',
  READ = 'read',
  FAILED = 'failed',
  EXPIRED = 'expired'
}

export interface NotificationTemplate {
  type: NotificationType;
  title: string;
  message: string;
  priority: NotificationPriority;
  expiresInHours?: number;
}

export interface NotificationJob {
  id: string;
  userId?: string;
  sentBy?: string; // Admin user who sent the notification
  type: NotificationType;
  title: string;
  message: string;
  data?: any;
  priority: NotificationPriority;
  expiresInHours?: number;
  scheduledFor?: Date;
  retryCount?: number;
  maxRetries?: number;
}

export interface NotificationBroadcast {
  type: NotificationType;
  title: string;
  message: string;
  data?: any;
  sentBy?: string; // Admin user who sent the notification
  priority: NotificationPriority;
  targetUsers?: string[]; // If empty, broadcast to all users
  expiresInHours?: number;
}
